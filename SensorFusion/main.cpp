#include <string>
#include <vector>
#include <fstream>
#include <iostream>
#include <sstream>
#include "Eigen/Eigen"
#include "interface/ground_truth_package.h"
#include "interface/measurement_package.h"
#include "sensorfusion.h"//同时包含了kalmanfilter.h

int main(int argc, char* argv[]) {

    // 设置毫米波雷达/激光雷达输入数据的路径
    // Set radar & lidar data file path
    std::string input_file_name = "../data/obj_pose-laser-radar-synthetic-input.txt";

    // 打开数据，若失败则输出失败信息，返回-1，并终止程序
    // Open file. if failed return -1 & end program
    std::ifstream input_file(input_file_name.c_str(), std::ifstream::in);//以只读的方式打开
    if (!input_file.is_open()) {
        std::cout << "Failed to open file named : " << input_file_name << std::endl;
        return -1;
    }

    // 分配内存
    // measurement_pack_list：毫米波雷达/激光雷达实际测得的数据。数据包含测量值和时间戳，即融合算法的输入。
    // groundtruth_pack_list：每次测量时，障碍物位置的真值。对比融合算法输出和真值的差别，用于评估融合算法结果的好坏。
    std::vector<MeasurementPackage> measurement_pack_list;
    std::vector<GroundTruthPackage> groundtruth_pack_list;

    // 通过while循环将雷达测量值和真值全部读入内存，存入measurement_pack_list和groundtruth_pack_list中
    // Store radar & lidar data into memory
    std::string line;
    while (getline(input_file, line)) { //line从inputfile中逐行读取数据
        std::string sensor_type;
        MeasurementPackage meas_package;
        GroundTruthPackage gt_package;
        std::istringstream iss(line); //iss从字符串line中逐个读取数据
        long long timestamp;

        // 读取当前行的第一个元素，L代表Lidar数据，R代表Radar数据
        // Reads first element from the current line. L stands for Lidar. R stands for Radar.
        iss >> sensor_type;
        if (sensor_type.compare("L") == 0) {
            // 激光雷达数据 Lidar data
            // 该行第二个元素为测量值x，第三个元素为测量值y，第四个元素为时间戳(纳秒）
            // 2nd element is x; 3rd element is y; 4th element is timestamp(nano second)
            meas_package.sensor_type_ = MeasurementPackage::LASER;
            meas_package.raw_measurements_ = Eigen::VectorXd(2);
            float x;
            float y;
            iss >> x;
            iss >> y;
            meas_package.raw_measurements_ << x, y;
            iss >> timestamp;
            meas_package.timestamp_ = timestamp;
            measurement_pack_list.push_back(meas_package);
        }
        else if (sensor_type.compare("R") == 0) {
            // 毫米波雷达数据 Radar data
            // 该行第二个元素为距离pho，第三个元素为角度phi，第四个元素为径向速度pho_dot，第五个元素为时间戳(纳秒）
            // 2nd element is pho; 3rd element is phi; 4th element is pho_dot; 5th element is timestamp(nano second)
            meas_package.sensor_type_ = MeasurementPackage::RADAR;
            meas_package.raw_measurements_ = Eigen::VectorXd(3);
            float rho;
            float phi;
            float rho_dot;
            iss >> rho;
            iss >> phi;
            iss >> rho_dot;
            meas_package.raw_measurements_ << rho, phi, rho_dot;
            iss >> timestamp;
            meas_package.timestamp_ = timestamp;
            measurement_pack_list.push_back(meas_package);
        }

        // 当前行的最后四个元素分别是x方向上的距离真值，y方向上的距离真值，x方向上的速度真值，y方向上的速度真值
        // read ground truth data to compare later
        float x_gt;
        float y_gt;
        float vx_gt;
        float vy_gt;
        iss >> x_gt;
        iss >> y_gt;
        iss >> vx_gt;
        iss >> vy_gt;
        gt_package.gt_values_ = Eigen::VectorXd(4);
        gt_package.gt_values_ << x_gt, y_gt, vx_gt, vy_gt;
        groundtruth_pack_list.push_back(gt_package);
    }

    std::cout << "Success to load data." << std::endl;

    // 开始部署跟踪算法
    SensorFusion fuser;
    std::ofstream outputfile("result.txt");//保存仿真结果
    for (size_t i = 0; i < measurement_pack_list.size(); ++i) {//size_t可以存储在理论上是可能的任何类型的数组的最大大小。
        fuser.Process(measurement_pack_list[i]);           //换句话说，一个指针可以被安全地放进为size_t类型;可理解为专门用来表示数组size的数据类型
        Eigen::Vector4d x_out = fuser.kf_.GetX();
        outputfile << x_out(0) << " "<< x_out(1) << " " << x_out(2) << " " << x_out(3) << " "
            <<groundtruth_pack_list[i].gt_values_(0) << " " <<
            groundtruth_pack_list[i].gt_values_(1) << " " <<
            groundtruth_pack_list[i].gt_values_(2) << " " <<
            groundtruth_pack_list[i].gt_values_(3) << std::endl;//前4个为估计值 后4个为真值
        
     /*   std::cout << "x " << x_out(0) 打印结果
            << " y " << x_out(1)
            << " vx " << x_out(2)
            << " vy " << x_out(3)
            << std::endl;
            */
    }
    outputfile.close();
}//main函数实现数据的读取(如果数据格式有变化只需改main而不用调整后续算法)，并调用SensorFusion，SensorFusion实现卡尔曼滤波的整体结构，包括协方差矩阵、噪声矩阵、状态转移矩阵等
//参数的设置（如果参数矩阵有变化只需更改SensorFusion而不影响kaiman里的公式），并在其中调用kalmanfilter，kalmanfilter中包含了具体的预测和更新公式，实现了算法的闭环。